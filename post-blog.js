(async () => {
  const browser = await puppeteer.launch({ headless: false });
  const page = await browser.newPage();

  try {
    console.log('네이버 로그인 시작...');
    await page.goto('https://nid.naver.com/nidlogin.login');

    // 로그인
    await page.type('input[name="id"]', NAVER_ID);
    await page.type('input[name="pw"]', NAVER_PW);
    await page.click('button[type="submit"]');
    await page.waitForNavigation();

    console.log('블로그 글쓰기 페이지 접속...');
    await page.goto(`https://blog.naver.com/${BLOG_ID}?Redirect=Write`, { waitUntil: 'networkidle2' });
    await new Promise(resolve => setTimeout(resolve, 5000));

    console.log('제목 입력 중...');
    // 제목 입력
    await page.evaluate((title) => {
      const titleArea = document.querySelector('[contenteditable]') ||
                       document.querySelector('textarea') ||
                       document.querySelector('input[type="text"]');
      if (titleArea) {
        titleArea.focus();
        titleArea.textContent = title;
        titleArea.value = title;
        titleArea.dispatchEvent(new Event('input', { bubbles: true }));
      }
    }, TITLE);

    await new Promise(resolve => setTimeout(resolve, 1000));

    console.log('본문 입력 중...');
    // 본문 입력
    await page.evaluate((content) => {
      const contentAreas = document.querySelectorAll('[contenteditable]');
      if (contentAreas.length > 1) {
        contentAreas[1].focus();
        contentAreas[1].textContent = content;
        contentAreas[1].dispatchEvent(new Event('input', { bubbles: true }));
      }
    }, CONTENT);

    await new Promise(resolve => setTimeout(resolve, 2000));

    console.log('발행 버튼 클릭...');
    // 발행 버튼 클릭
    await page.click('button:contains("발행")') ||
             await page.evaluate(() => {
               const buttons = Array.from(document.querySelectorAll('button'));
               const publishBtn = buttons.find(b => b.textContent.includes('발행'));
               if (publishBtn) publishBtn.click();
             });

    await page.waitForTimeout(3000);
    console.log('✅ 블로그 포스팅 완료!');

  } catch (error) {
    console.error('❌ 오류:', error);
  }

  await browser.close();
})();
